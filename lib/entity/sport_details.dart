import 'package:flutter/material.dart';


class SportDetails{
    String _name;
    String _desc;
    String _result;

    String get name => _name;

    set name(String value) {
      _name = value;
    }

    String get desc => _desc;

    set desc(String value) {
      _desc = value;
    }

    String get result => _result;

    set result(String value) {
      _result = value;
    }


}